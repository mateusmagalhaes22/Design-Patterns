package factory;

public class Carro {
	
	String marca;
	String modelo;
	String feitoEm;
	String cor;
	int kms;
	
	public Carro(String marca, String modelo, String feitoEm, String cor, int kms) {
		this.marca = marca;
		this.modelo = modelo;
		this.feitoEm = feitoEm;
		this.cor = cor;
		this.kms = kms;
	}
	
}
